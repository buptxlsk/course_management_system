-- 切换到 student 数据库
USE student;

-- 创建学生表 S
CREATE TABLE S (
    SNO CHAR(10),
    SNAME CHAR(8),
    SEX CHAR(2),
    AGE CHAR(2),
    SDEPT CHAR(10),
    LOGN CHAR(20),
    PSWD CHAR(20),
    PRIMARY KEY(SNO)
) DEFAULT CHARSET=utf8;

-- 创建教师表 T
CREATE TABLE T (
    TNO CHAR(10),
    TNAME CHAR(8),
    LOGN CHAR(20),
    PSWD CHAR(20),
    PRIMARY KEY(TNO)
) DEFAULT CHARSET=utf8;


-- 创建课程表 C
CREATE TABLE C (
    CNO CHAR(10),
    CNAME CHAR(20),
    CREDI INT(11),
    CDEPT CHAR(50),
    TNAME CHAR(8),
    PRIMARY KEY(CNO)
) DEFAULT CHARSET=utf8;

-- 创建学生-课程关系表 SC
CREATE TABLE SC (
    SNO CHAR(10),
    CNO CHAR(10),
    GRADE INT(11),
    PRIMARY KEY(SNO, CNO),
    FOREIGN KEY(CNO) REFERENCES C(CNO),
    FOREIGN KEY(SNO) REFERENCES S(SNO)
) DEFAULT CHARSET=utf8;

